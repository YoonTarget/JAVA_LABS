package loopexample;

public class ForExample2 {

	public static void main(String[] args) {
		
		int i;
		
		for(i = 0; i < 10; i ++) {
			System.out.println("Hello, World!");
		} //프로그래머들이 더 선호하는 방식
		
		System.out.println();
		
		for(i = 1; i <= 10; i ++) {
			System.out.println("Hello, World!");
		}

	}

}
